window.onload = function() {
  // TODO: Add a way to add comments in other languages
  const comments = document.getElementById('comments');

  if (comments) {
    const currLoc = location.href.split('/');
    currLoc.pop();
    const postName = currLoc.pop();
    // Create add button
    const addButton = document.createElement('a');
    addButton.className = 'add-comment-button';
    addButton.innerText = 'add comment';
    addButton.href = 'https://github.com/savio-henrique/personal-hugo-blog/blob/main/content/en/posts/' + postName + '.en.md?plain=1#comments';
    comments.appendChild(addButton);
  };
};
